"""
02_export_constants_to_verse.py -- exporter Python que genera 12 archivos .verse
en Content/Verse/Generated/ desde los JSONs en data/.

Patron Source -> Transformer -> Artifact (BOOTSTRAP_PIPELINE.md §1).
Todos los archivos generados llevan header "AUTO-GENERATED. DO NOT EDIT MANUALLY."

Uso:
    python scripts/build/02_export_constants_to_verse.py [--only FUNC] [--dry-run]

Funciones de export (12):
    - export_companions()       -> Companions_Generated.verse
    - export_items()            -> Items_Generated.verse
    - export_prices()           -> Prices_Generated.verse
    - export_quests()           -> Quests_Generated.verse
    - export_battle_pass()      -> BattlePass_Generated.verse
    - export_player_stats()     -> PlayerStats_Generated.verse
    - export_skill_tree()       -> SkillTree_Generated.verse
    - export_achievements()     -> Achievements_Generated.verse
    - export_localization()     -> Localization_Generated.verse
    - export_module_registry()  -> ModuleRegistryConstants.verse  [arquitectura]
    - export_event_payloads()   -> EventPayloads_Generated.verse  [arquitectura]
    - export_event_bus()        -> EventBusConstants.verse        [arquitectura]

Reglas naming:
    10 archivos llevan sufijo "_Generated.verse" (datos).
    2 excepciones canonicas (D-A10): ModuleRegistryConstants.verse, EventBusConstants.verse.

Estrategia para sources que aun no existen:
    Cada export_* detecta FileNotFoundError, imprime [INFO] saltando, y genera un
    archivo .verse placeholder con header AUTO-GENERATED + comentario TODO SPR-XXX.
    El script NO falla por source faltante (es esperado en F0). Idempotente.

Exit codes:
    0 = todos los exports OK (incluye placeholders por sources faltantes)
    1 = error real (JSON malformado en source existente, write error, etc.)
    2 = config invalida (--only con nombre desconocido)
"""
