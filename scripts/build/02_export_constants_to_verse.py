def _rel(p: Path) -> str:
    """Ruta relativa al repo en estilo POSIX (slash forward)."""
    try:
        return p.relative_to(REPO_ROOT).as_posix()
    except ValueError:
        return p.as_posix()


def _write(output: Path, content: str, label: str) -> None:
    """Escribe content a output salvo en --dry-run. Reporta [OK] con bytes."""
    size = len(content.encode("utf-8"))
    if DRY_RUN:
        print(f"[OK] (dry-run) {_rel(output)} ({size} bytes, {label})")
        return
    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text(content, encoding="utf-8")
    print(f"[OK] {_rel(output)} ({size} bytes, {label})")


def _placeholder(source: Path, output: Path, func_name: str, sprint_token: str) -> int:
    """Genera placeholder estandar cuando la source no existe aun."""
    print(f"[INFO] {_rel(source)} no existe aun ({sprint_token}), generando placeholder")
    body = (
        WARNING_HEADER_TEMPLATE.format(source=_rel(source), func_name=func_name)
        + "\n"
        f"# TODO {sprint_token}: source pendiente, archivo placeholder sin contenido.\n"
    )
    _write(output, body, f"placeholder TODO {sprint_token}")
    return 0


def _load_json(source: Path) -> dict[str, Any]:
    """Lee JSON UTF-8. Lanza FileNotFoundError si no existe; json.JSONDecodeError si invalido."""
    with source.open("r", encoding="utf-8") as f:
        return json.load(f)


def _to_pascal_case(name: str) -> str:
    """Convierte snake_case o SNAKE_CASE a PascalCase. Ej: DRAGON_FIRE -> DragonFire."""
    return "".join(part.capitalize() for part in name.split("_") if part)


