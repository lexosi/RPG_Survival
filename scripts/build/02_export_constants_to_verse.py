def main() -> int:
    global DRY_RUN
    parser = argparse.ArgumentParser(
        description="Export JSONs to Verse constants (12 archivos generados).",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=(
            "Exit codes:\n"
            "  0 = todos los exports OK (incluye placeholders)\n"
            "  1 = error real (JSON malformado, write error)\n"
            "  2 = config invalida (--only con nombre desconocido)"
        ),
    )
    parser.add_argument("--only", help="Ejecutar solo una funcion export (debug)")
    parser.add_argument("--dry-run", action="store_true",
                        help="Imprime que se generaria sin escribir archivos")
    args = parser.parse_args()

    DRY_RUN = bool(args.dry_run)

    if args.only is not None:
        if args.only not in EXPORTS:
            print(f"[FAIL] --only '{args.only}' desconocido. Validas: {sorted(EXPORTS)}", file=sys.stderr)
            return 2
        targets = {args.only: EXPORTS[args.only]}
    else:
        targets = EXPORTS

    print(f"[INFO] {len(targets)} export(s) a ejecutar (dry_run={DRY_RUN})")
    if not DRY_RUN:
        GENERATED_ROOT.mkdir(parents=True, exist_ok=True)

    failures: list[tuple[str, str]] = []
    for name, fn in targets.items():
        try:
            rc = fn()
            if rc != 0:
                failures.append((name, f"return code {rc}"))
        except json.JSONDecodeError as e:
            failures.append((name, f"JSONDecodeError: {e}"))
            print(f"[FAIL] {name}: JSON malformado -- {e}", file=sys.stderr)
        except OSError as e:
            failures.append((name, f"OSError: {e}"))
            print(f"[FAIL] {name}: I/O error -- {e}", file=sys.stderr)
        except Exception as e:
            failures.append((name, f"{type(e).__name__}: {e}"))
            print(f"[FAIL] {name}: {type(e).__name__} -- {e}", file=sys.stderr)

    print()
    if failures:
        print(f"[FAIL] {len(failures)} export(s) fallaron:")
        for name, err in failures:
            print(f"   - {name}: {err}")
        return 1
    print(f"[OK] {len(targets)} export(s) completados")
    return 0


if __name__ == "__main__":
    sys.exit(main())
