def export_companions() -> int:
    source = DATA_ROOT / "companions" / "companions_base.json"
    output = GENERATED_ROOT / "Companions_Generated.verse"

    if not source.exists():
        return _placeholder(source, output, "export_companions", "SPR-002")

    data = _load_json(source)
    companions = data.get("companions", [])

    header = (
        "# ============================================================\n"
        "# AUTO-GENERATED FILE. DO NOT EDIT MANUALLY.\n"
        f"# Source: {_rel(source)}\n"
        "# Generated by: 02_export_constants_to_verse.py (export_companions)\n"
        "# See BOOTSTRAP_PIPELINE.md for rationale.\n"
        "# Refactor SPR-211: structs literales top-level propagan transacts\n"
        "# (err 3512). API canonica = funciones getter, no constantes nombradas.\n"
        "# Consumers: Companions_Generated.GetCompanion<NAME>() o\n"
        "# Companions_Generated.GetAllCompanions().\n"
        "# ============================================================"
    )

    lines: list[str] = [header, ""]
    lines.append("companion_def<public> := struct:")
    lines.append("    ID:int")
    lines.append("    Rarity:int")
    lines.append("    BaseHP:int")
    lines.append("    BaseAtk:int")
    lines.append("    BaseDef:int")
    lines.append("    BaseSpeed:int")
    lines.append("")
    lines.append("Companions_Generated<public> := module:")

    getter_names: list[str] = []
    for c in companions:
        pascal = _to_pascal_case(c["name"])
        getter = f"GetCompanion{pascal}"
        getter_names.append(getter)
        lines.append("")
        lines.append(f"    {getter}<public>():companion_def=")
        lines.append(
            f"        companion_def{{"
            f"ID := {c['id']}, "
            f"Rarity := {c['rarity']}, "
            f"BaseHP := {c['base_hp']}, "
            f"BaseAtk := {c['base_atk']}, "
            f"BaseDef := {c['base_def']}, "
            f"BaseSpeed := {c['base_speed']}"
            f"}}"
        )

    lines.append("")
    lines.append("    GetAllCompanions<public>():[]companion_def=")
    if getter_names:
        calls = ", ".join(f"{n}()" for n in getter_names)
        lines.append(f"        array{{ {calls} }}")
    else:
        lines.append("        array{}")

    body = "\n".join(lines)
    _write(output, body, f"real, {len(companions)} companions")
    return 0


